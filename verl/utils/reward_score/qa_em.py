import re
import string
import random
from collections import Counter

def normalize_answer(s):
    def remove_articles(text):
        return re.sub(r"\b(a|an|the)\b", " ", text)

    def white_space_fix(text):
        return " ".join(text.split())

    def remove_punc(text):
        exclude = set(string.punctuation)
        return "".join(ch for ch in text if ch not in exclude)

    def lower(text):
        return text.lower()

    return white_space_fix(remove_articles(remove_punc(lower(s))))


def em_check(prediction, golden_answers):
    if isinstance(golden_answers, str):
        golden_answers = [golden_answers]
    normalized_prediction = normalize_answer(prediction)
    score = 0
    for golden_answer in golden_answers:
        golden_answer = normalize_answer(golden_answer)
        if golden_answer == normalized_prediction:
            score = 1
            break
    return score


def f1_check(prediction, golden_answers):
    """Return the maximum word-level F1 over all reference answers."""
    if prediction is None:
        return 0.0
    if isinstance(golden_answers, str):
        golden_answers = [golden_answers]

    prediction_tokens = normalize_answer(str(prediction)).split()
    best_score = 0.0
    for golden_answer in golden_answers:
        golden_tokens = normalize_answer(str(golden_answer)).split()
        if not prediction_tokens or not golden_tokens:
            score = float(prediction_tokens == golden_tokens)
        else:
            overlap = Counter(prediction_tokens) & Counter(golden_tokens)
            num_same = sum(overlap.values())
            if num_same == 0:
                score = 0.0
            else:
                precision = num_same / len(prediction_tokens)
                recall = num_same / len(golden_tokens)
                score = 2 * precision * recall / (precision + recall)
        best_score = max(best_score, score)
    return best_score


