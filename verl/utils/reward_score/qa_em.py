def extract_solution(solution_str):
    """Extract the equation from the solution string."""
    # Remove everything before the first "Assistant:"
    # if "Assistant:" in solution_str:
    #     solution_str = solution_str.split("Assistant:", 1)[1]
    # elif "<|im_start|>assistant" in solution_str:
    #     solution_str = solution_str.split("<|im_start|>assistant", 1)[1]
    # else:
    #     return None
    # solution_str = solution_str.split('\n')[-1]

    assistant_match = re.search(r"<\|im_start\|>assistant\s*", solution_str)
    if assistant_match:
        solution_str = solution_str[assistant_match.end():]

    matches = list(re.finditer(r'<answer>(.*?)</answer>', solution_str, re.DOTALL))

    if not matches:
        return None

    return matches[-1].group(1).strip()


def extract_last_answer(text):
    """Extract the last answer block from model response text."""
    if text is None:
        return None
    matches = list(re.finditer(r'<answer>(.*?)</answer>', text, re.DOTALL))
    if not matches:
        return None
    return matches[-1].group(1).strip()


